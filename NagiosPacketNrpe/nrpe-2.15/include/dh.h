#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xFA,0xA4,0x69,0x38,0x6C,0x87,0x62,0x2C,0x6B,0xA5,0x37,0x24,
		0x7C,0x47,0x56,0x8D,0xF6,0x2E,0x44,0xB6,0x14,0xB7,0x8C,0xD6,
		0x2D,0x46,0x5C,0xD6,0xE9,0x17,0x75,0x21,0xC2,0x43,0x8A,0x39,
		0xA9,0x49,0x79,0x45,0x0E,0xEA,0xFA,0x82,0x26,0x30,0x8F,0xCC,
		0xBF,0x1D,0x94,0x58,0x91,0x64,0x0D,0x06,0x23,0x22,0x91,0x16,
		0xAC,0xF1,0x1B,0x93,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
